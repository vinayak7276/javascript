let day=1;
switch(day){
    case 1:
        console.log("Monday");
        case 2:
        console.log("Tuesday");
        case 3:
        console.log("Wednesday");
        case 4:
        console.log("Thursday");
        case 5:
        console.log("Friday");
        case 6:
        console.log("Saturday");
        case 7:
        console.log("Sunday");
        break;
        default:
            console.log("This is not day");
}